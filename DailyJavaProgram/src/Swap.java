
public class Swap
  {
    public static void main(String[] args)
   

          {
                   

            int x=8;
            int y=22;
          System.out.println("Before swapping \nX="+ x + "\nY="+  y);
         
          x=x+y;
          y=x-y;
          x=x-y;

          System.out.println("Before swapping \nX="+ x + "\nY="+  y);
}
}

