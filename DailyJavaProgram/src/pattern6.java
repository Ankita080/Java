
public class pattern6 {

	public static void main(String[] args) {


for(char ab='A';ab<='D';ab++)
			
		{
			for(char abc='A';abc<=ab;abc++) {
				
				System.out.print(abc);
			}
            System.out.println();
			
		}
for(char ab='D';ab>='A';ab--)
	
{
	for(char abc='A';abc<=ab;abc++) {
		
		System.out.print(abc);
	}
    System.out.println();
	
}
	
	
 }
}
